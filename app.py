def _find_candidates(user_message, records):
    """
    ‡∏´‡∏≤‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤‡∏ó‡∏µ‡πà‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤
    """
    candidates = []
    for row in records:
        name = str(row.get("‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤", "")).strip()
        link = str(row.get("‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö", "")).strip()   # ‡πÉ‡∏ä‡πâ "‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö" ‡πÄ‡∏õ‡πá‡∏ô‡∏•‡∏¥‡πâ‡∏á‡∏Å‡πå

        if not name:
            continue

        # ‡∏ï‡∏£‡∏á‡∏ä‡∏∑‡πà‡∏≠ 100%
        if user_message == name:
            return [{"name": name, "link": link}]

        # ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ user_message ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏ä‡∏∑‡πà‡∏≠‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤
        if user_message in name:
            candidates.append({"name": name, "link": link})

        # ‡∏ñ‡πâ‡∏≤ user_message ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏Ñ‡∏µ‡∏¢‡πå‡πÄ‡∏ß‡∏¥‡∏£‡πå‡∏î
        kw_raw = str(row.get("‡∏Ñ‡∏µ‡∏¢‡πå‡πÄ‡∏ß‡∏¥‡∏£‡πå‡∏î", ""))
        kws = [k.strip() for k in kw_raw.split(",") if k.strip()]
        for kw in kws:
            if kw and kw in user_message:
                candidates.append({"name": name, "link": link})
                break

    return candidates


